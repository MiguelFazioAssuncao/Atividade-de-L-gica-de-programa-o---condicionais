let A = Number(prompt("Digite o valor de A")), B = Number(prompt("Digite o valor de B")), C = Number(prompt("Digite o valor de C"))

if(A + B > C){
    alert("A soma entre A e B é maior que C")
} else if(A + B < C){
    alert("A soma entre A e B é menor que C")
} else{
    alert("A soma entre A e B é igual a C")
}